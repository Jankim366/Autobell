#ifdef EN_USART2_RX   	//如果使能了接收
//接收缓存区
u8 USART2_RX_BUF[USART2_RX_BUF_SIZE];  	//接收缓冲,最大USART2_RX_BUF_SIZE个字节.
//接收到的数据长度
u16 USART2_RX_CNT=0;   
void USART2_IRQHandler(void)
{
	u8 res;	    
#ifdef OS_CRITICAL_METHOD 	//如果OS_CRITICAL_METHOD定义了,说明使用ucosII了.
	OSIntEnter();    
#endif
	if(USART2->SR&(1<<5))//接收到数据
	{	 
		res=USART2->DR; 			 
		if(USART2_RX_CNT<USART2_RX_BUF_SIZE)
		{
			USART2_RX_BUF[USART2_RX_CNT]=res;		//记录接收到的值
			USART2_RX_CNT++;						//接收数据增加1 
		} 
	}  											 
#ifdef OS_CRITICAL_METHOD 	//如果OS_CRITICAL_METHOD定义了,说明使用ucosII了.
	OSIntExit();  											 
#endif
} 
#endif										 
//初始化IO 串口2
//pclk1:PCLK1时钟频率(Mhz)
//bound:波特率	  
void usart2_init(u32 pclk1,u32 bound)
{  	 
	float temp;
	u16 mantissa;
	u16 fraction;	   
	temp=(float)(pclk1*1000000)/(bound*16);//得到USARTDIV
	mantissa=(u16)temp;				 //得到整数部分
	fraction=(u16)(temp-mantissa)*16; //得到小数部分	 
    mantissa<<=4;
	mantissa+=fraction; 
	// RCC->APB2ENR|=1<<8;   	//使能PORTG口时钟  
 	// GPIOG->CRH&=0XFFFFFF0F;	//IO状态设置
	// GPIOG->CRH|=0X00000030;	//IO状态设置	  
 
	RCC->APB2ENR|=1<<2;   	//使能PORTA口时钟  
	GPIOA->CRL&=0XFFFF00FF;	//IO状态设置	PA2、PA3
	GPIOA->CRL|=0X00008B00;	//IO状态设置	  

	RCC->APB1ENR|=1<<17;  	//使能串口时钟	USART2
		  
	RCC->APB1RSTR|=1<<17;   //复位串口2
	RCC->APB1RSTR&=~(1<<17);//停止复位	   	   
	//波特率设置
 	USART2->BRR=mantissa; 	// 波特率设置	 
	USART2->CR1|=0X200C;  	//1位停止,无校验位.
#ifdef EN_USART2_RX		  	//如果使能了接收
	//使能接收中断
	USART2->CR1|=1<<8;    	//PE中断使能
	USART2->CR1|=1<<5;    	//接收缓冲区非空中断使能	    	
	MY_NVIC_Init(3,3,USART2_IRQn,2);//组2，最低优先级 
#endif
	USART2_TX_EN=0;			//默认为接收模式	
}

//USART2发送len个字节.
//buf:发送区首地址
//len:发送的字节数(为了和本代码的接收匹配,这里建议不要超过64个字节)
void USART2_Send_Data(u8 *buf,u16 len)
{
	u16 t;
	USART2_TX_EN=1;			//设置为发送模式
  	for(t=0;t<len;t++)		//循环发送数据
	{
		while ((USART2->SR&0X40)==0);//等待发送结束		  
		USART2->DR=buf[t];
	}	 
	while ((USART2->SR&0X40)==0);//等待发送结束	
	USART2_RX_CNT=0;	  
	USART2_TX_EN=0;				//设置为接收模式	
}
//USART2查询接收到的数据
//buf:接收缓存首地址
//len:读到的数据长度
void USART2_Receive_Data(u8 *buf,u16 *len)
{
	u16 rxlen=USART2_RX_CNT;
	u16 i=0;
	*len=0;				//默认为0
	//delay_ms(10);		//等待10ms,连续超过10ms没有接收到一个数据,则认为接收结束
	if(rxlen==USART2_RX_CNT&&rxlen)//接收到了数据,且接收完成了
	{
		for(i=0;i<rxlen;i++)
		{
			buf[i]=USART2_RX_BUF[i];	
		}		
		*len=USART2_RX_CNT;	//记录本次数据长度
		USART2_RX_CNT=0;		//清零
	}
}





